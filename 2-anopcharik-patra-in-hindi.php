        <h3>Key Takeaways from Letter 2</h3>
        <ul>
            <li><strong>Affectionate Opening:</strong> "प्रिय अनुज" (Dear younger brother) and "शुभ आशीर्वाद" (Auspicious blessings).</li>
            <li><strong>Authoritative Tone:</strong> Notice the use of "सख्त निर्देश" (Strict instruction) and "खिलवाड़ कर रहे हो" (Playing around with). The writer is taking full command of the situation as an elder.</li>
            <li><strong>Protective Closing:</strong> "तुम्हारा बड़ा भाई" establishes a protective, guiding stance rather than an obedient one.</li>
