            आर्यन, मैं तुम्हें कई बार समझा चुका हूँ कि बाहरी और खुले में बिकने वाला अस्वच्छ भोजन खाना बंद करो। तुम अपनी चटपटी जीभ के स्वाद के लिए अपने स्वास्थ्य के साथ खिलवाड़ कर रहे हो। यह बात हमेशा याद रखना कि एक कमज़ोर और बीमार शरीर के साथ तुम कभी भी अपनी शिक्षा पर ध्यान केंद्रित नहीं कर पाओगे। आज से ही बाहर का जंक फूड और दूषित जल पीना बिल्कुल बंद कर दो। केवल हॉस्टल के मेस का बना हुआ सादा और स्वच्छ भोजन ही प्रयोग करो। रोज़ सुबह उठकर व्यायाम करो और ताज़े फल खाओ।<br><br>

            मेरा यह सख्त निर्देश है कि तुम डॉक्टर द्वारा दी गई सभी दवाइयाँ समय पर लो और पूरी तरह आराम करो। मुझे तुरंत पत्र लिखकर अपने वर्तमान स्वास्थ्य की जानकारी देना ताकि माताजी की चिंता कुछ कम हो सके।<br><br>

            तुम्हारा बड़ा भाई,<br>
            विशाल
