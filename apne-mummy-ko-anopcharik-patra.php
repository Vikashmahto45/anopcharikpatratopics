            माँ, मैं अपनी गलती स्वीकार करता हूँ कि पिछले कुछ महीनों में मेरा ध्यान पढ़ाई से भटक गया था। मैं विद्यालय की खेलकूद प्रतियोगिताओं और स्कूल के वार्षिकोत्सव (Annual Function) की तैयारियों में इतना अधिक व्यस्त हो गया था कि मैंने अपनी नियमित पढ़ाई की ओर बिल्कुल भी ध्यान नहीं दिया। मुझे यह भ्रम हो गया था कि मैं परीक्षा से कुछ दिन पहले पढ़कर अच्छे अंक ले आऊँगा, जो कि मेरी सबसे बड़ी भूल साबित हुई। आपके सभी निर्देश और चेतावनियाँ एकदम सही थीं जिन्हें मैंने नजरअंदाज कर दिया।<br><br>

            मैं आपको आश्वस्त (assure) करता हूँ कि यह मेरे जीवन की पहली और अंतिम लापरवाही थी। मैंने आज से ही अपनी खेलकूद की गतिविधियों को रोक दिया है और एक नई समय-सारणी (timetable) बनाकर पढ़ाई शुरू कर दी है। मैं आपको वचन देता हूँ कि अंतिम बोर्ड परीक्षाओं में मैं अपने शत-प्रतिशत प्रयास से शानदार अंक प्राप्त करूँगा और आपको निराश होने का कोई भी अवसर नहीं दूँगा।<br><br>

            यह भूल समझकर मुझे क्षमा कर दें। पिताजी को मेरा चरण-स्पर्श कहना।<br><br>

