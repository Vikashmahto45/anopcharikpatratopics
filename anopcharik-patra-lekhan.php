        <h2>Preparing for the Examinations</h2>
        <p>As you prepare for your finals, do not just memorize formats. Practice the actual <em>lekhan</em> process. Write out at least five letters using different tones—one advisory, one congratulatory, one condolence, one invitation, and one descriptive. This will build your mental dictionary of vocabulary, ensuring that no matter what prompt the examiner presents, you possess the words to articulate your thoughts beautifully.</p>

