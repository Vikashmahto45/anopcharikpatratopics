            <div class="prose-block">
                In the final section of the <strong>atmatran class 10 summary</strong>, Tagore says that even when the
                whole world deceives him and he faces immense sorrow, he should not doubt God's kindness. He wants to
                keep his head high and his mind steady. This prayer shifts the focus from 'Divine Intervention' to
                'Self-Reliance' (Atma-Nirbharta).
            </div>
        </section>

        <section class="chapter-card">
            <h2 class="chapter-header">आत्मशक्ति का आवाहन (Inquiry of Inner Strength)</h2>
            <div class="prose-block">
                The core of the <strong>atmatran summary class 10</strong> revolves around the poet's refusal to ask for 
                'Saantvana' (consolation) when his heart is burdened with grief. Tagore states that if God cannot 
                remove the grief, he should at least grant the strength to overcome it. He wants to remain steady 
                even if the whole world turns against him. 
            </div>
            <div class="prayer-box">
                "दु:ख-ताप से व्यथित चित्त को न दो सांत्वना नहीं सही, पर इतना होवे—दु:ख को मैं कर सकूँ सदा जय।" (Do not 
                give consolation to my heat-stricken mind—only let me be able to win over the sorrow forever).
            </div>
            <div class="prose-block">
                This perspective is revolutionary in religious literature. Usually, a devotee asks God to be their 
                protector; here, the devotee asks God to be their trainer. He wants to be a "Karmayogi" who performs 
                his duties without fear of failure. Even if he gets cheated by everyone, he prays that his faith in 
                truth and in God remains unshaken. This makes 'Atmatran' (Self-Rescue) a powerful guide for modern 
                life. 
            </div>
        </section>

