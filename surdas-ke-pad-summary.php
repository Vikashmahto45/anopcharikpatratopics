            <h2 class="poetry-header">प्रथम पद: 'ऊधौ, तुम हौ अति बड़भागी'</h2>
            <div class="pad-box">
                <span class="pad-hindi">ऊधौ, तुम हौ अति बड़भागी।<br>अपरस रहत सनेह तगा तैं, नाहिन मन अनुरागी।</span>
                In the first pad, the Gopis mock Uddhav by calling him "fortunate." They satirically say he is like a
                lotus leaf in water (unsouched by it) because even after staying close to Krishna (the ocean of love),
                he remains untouched by love. This irony highlights the Gopis' belief that a life without love/bhakti is
                empty.
            </div>
        </section>

        <section class="poetry-card">
            <h2 class="poetry-header">द्वितीय पद: 'मन की मन ही माँझ रही'</h2>
            <div class="pad-box">
                <span class="pad-hindi">मन की मन ही माँझ रही।<br>कहिए जाइ कौन पै ऊधौ, नाहीं परत कही।</span>
                The Gopis express their pain that their messages and emotions for Krishna remained hidden in their
                hearts. They expected Krishna to return and soothe their pain, but he sent Uddhav with Yoga messages,
                which acted like pouring ghee into the fire of their separation.
            </div>
        </section>

        <section class="poetry-card">
            <h2 class="poetry-header">तृतीय पद: 'हमारैं हरि हारिल की लकरी'</h2>
            <div class="pad-box">
                <span class="pad-hindi">हमारैं हरि हारिल की लकरी।<br>मन क्रम वचन नंद-नंदन उर, यह करि पकरी।</span>
                In the third pad, the Gopis compare their love for Krishna to the 'Haril' bird, which never lets go of a
                wooden stick. They have caught Krishna in their hearts through mind, action, and speech. They find
                Uddhav’s Yoga messages as bitter as a "karvi kakri" (bitter gourd), showing their total rejection of
                dry intellectualism.
            </div>
        </section>

        <section class="poetry-card">
            <h2 class="poetry-header">चतुर्थ पद: 'हरि हैं राजनीति पढ़ि आए'</h2>
            <div class="pad-box">
                <span class="pad-hindi">हरि हैं राजनीति पढ़ि आए।<br>समुझी बात कहत मधुकर के, समाचार सब पाए।</span>
                In the final pad, the Gopis sarcastically remark that Krishna has now studied "Politics" in Mathura.
                They complain that he was already clever, and now his intelligence has grown so much that he sends
                Yoga messages to simple women. They conclude by reminding Krishna of 'Raj Dharma'—the duty of a king
                is to protect his subjects from suffering, not to cause it.
            </div>
        </section>

