        <h2>The Official Academic Definition (किसे कहते हैं?)</h2>
        <p>In standard Hindi grammar (Vyakaran), the definition is universally taught as follows:</p>

        <div class="info-box"
            style="font-size: 1.25rem; padding: 25px; line-height: 1.8; text-align: center; border: 2px solid var(--brand-primary); background: #f0fdf4; margin-bottom: 30px;">
