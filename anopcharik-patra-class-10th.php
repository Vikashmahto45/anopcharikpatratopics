        <h2>Understanding the Class 10th Marking Scheme</h2>
        <p>Before you begin writing, you must know how your letter will be judged. The CBSE board typically allocates 5 marks for the letter-writing section, divided strictly among three parameters:</p>

        <ul style="background: var(--surface); padding: 25px 40px; border: 1px solid var(--border-color); border-radius: var(--radius-md);">
            <li style="margin-bottom: 15px;"><strong>Format (प्रारूप) - 1 Mark:</strong> This mark is strictly objective. If you correctly left-align the sender's address, the date, the salutation (संबोधन), the greeting (अभिवादन), the closing, and the name without any spelling errors, you secure this mark. If you mistakenly write a 'Subject' (विषय), this mark is immediately zeroed.</li>
            <li style="margin-bottom: 15px;"><strong>Content (विषय-वस्तु) - 3 Marks:</strong> This evaluates <em>what</em> you are saying. Have you directly addressed the prompt? If the prompt asked you to advise a friend on time management, did you provide logical, compelling reasons? Did you divide the content into three distinct paragraphs (Introduction, Core Message, Conclusion)?</li>
            <li style="margin-bottom: 15px;"><strong>Expression & Accuracy (भाषा और प्रस्तुति) - 1 Mark:</strong> This is where <strong>anopcharik patra class 10th</strong> students lose the most points. This mark tests your vocabulary, grammatical accuracy, use of idioms (मुहावरे), and spelling. Using basic, repetitive sentences will not earn you this mark.</li>
        </ul>

