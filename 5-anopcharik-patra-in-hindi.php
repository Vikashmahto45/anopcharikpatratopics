        <h2>5. The Descriptive Letter (To a Friend)</h2>
        <p>Narrative writing testing adjectives and past-tense consistency.</p>

        <div class="letter-box">
            <strong>प्रश्न:</strong> किसी पर्वतीय स्थल (Hill Station) की अपनी हाल की यात्रा का रोमांचक वर्णन करते हुए अपने मित्र को एक पत्र लिखिए।<br><br>

            ३४, एमजी रोड,<br>
            बेंगलुरु - ५६०००१<br><br>

            दिनांक: १५ जुलाई २०२६<br><br>

            प्रिय मित्र अंशुल,<br>
            सप्रेम नमस्ते।<br><br>

            मैं यहाँ ठीक हूँ और तुम्हारी कुशलता की कामना करता हूँ। मैं तुम्हें यह बताने के लिए अत्यंत उत्साहित हूँ कि पिछले सप्ताह मैं अपने पूरे परिवार के साथ हिमाचल प्रदेश के एक अत्यंत सुंदर पर्वतीय स्थल, मनाली (Manali), घूमने गया था। यह मेरी जीवन की सबसे रोमांचकारी (adventurous) यात्राओं में से एक रही।<br><br>

            हम ट्रेन से चंडीगढ़ गए और वहाँ से बस द्वारा मनाली पहुँचे। जैसे-जैसे बस पहाड़ों पर चढ़ रही थी, चारों ओर हरे-भरे देवदार के ऊँचे पेड़ और गहरी घाटियों का नज़ारा मन मोह रहा था। मनाली पहुँचकर ऐसा लगा मानो हम बादलों के बीच आ गए हों। वहाँ की ठंडी और शुद्ध हवा ने हमारी सारी शारीरिक थकान मिटा दी। हमने वहाँ प्रसिद्ध हिडिम्बा देवी मंदिर के दर्शन किए। सबसे अधिक मज़ा हमें रोहतांग पास (Rohtang Pass) में आया, जहाँ हमने चारों तरफ बर्फ ही बर्फ देखी। मैंने जीवन में पहली बार बर्फ के साथ खेलते हुए स्केटिंग (skating) का भी आनंद लिया। मॉल रोड (Mall Road) पर घूमते समय मैंने तुम्हारे लिए भी लकड़ी का एक बहुत सुंदर स्मृति-चिन्ह (souvenir) खरीदा है।<br><br>

            मनाली की वो खूबसूरत वादियाँ और सर्द हवाएँ मुझे आज भी याद आ रही हैं। काश, तुम भी इस यात्रा में मेरे साथ होते तो आनंद दोगुना हो जाता। अगली बार हम साथ चलने की योजना अवश्य बनाएंगे।<br><br>

            अंकल-आंटी को सादर प्रणाम कहना। जब मैं तुमसे मिलूँगा तो सारी तस्वीरें भी दिखाऊँगा।<br><br>

            तुम्हारा अभिन मित्र,<br>
            रमन
        </div>

