        <h2>2. The Advisory Letter (To Younger Sibling)</h2>
        <p>This letter is critical because it tests your ability to provide structured, logical advice within an informal setting. It frequently targets topics like junk food, internet addiction, or bad company.</p>

        <div class="letter-box">
            <strong>प्रश्न:</strong> अपने छोटे भाई को संतुलित आहार (Balanced Diet) खाने के महत्व और जंक फूड (Junk Food) के नुकसान बताते हुए एक सलाह पत्र लिखिए।<br><br>

            ५६-ए, गोमती नगर,<br>
            लखनऊ - २२६०१०<br><br>

            दिनांक: ०५ मार्च २०२६<br><br>

            प्रिय अनुज आर्यन,<br>
            शुभाशीष।<br><br>

            मैं यहाँ ठीक हूँ और आशा करता हूँ कि तुम्हारी पढ़ाई भी अच्छी चल रही होगी। कल माताजी का पत्र प्राप्त हुआ, जिसे पढ़कर मुझे बहुत चिंता हुई। उन्होंने लिखा है कि आजकल तुमने घर का बना पौष्टिक भोजन खाना लगभग बंद कर दिया है और तुम रोज़ाना केवल पिज़्ज़ा, बर्गर और चाऊमीन जैसे जंक फूड (Junk Food) ही खाते हो। इसके कारण तुम्हें बार-बार पेट दर्द की शिकायत भी रहने लगी है।<br><br>

            आर्यन, मैं तुम्हें यह समझाना चाहता हूँ कि बाहरी जंक फूड स्वाद में भले ही अच्छे लगें, लेकिन स्वास्थ्य के लिए ये धीमे ज़हर (slow poison) के समान हैं। इनमें न तो कोई विटामिन्स होते हैं और न ही पोषक तत्व। लगातार इनका सेवन करने से शारीरिक कमज़ोरी, मोटापा और कई गंभीर बीमारियाँ जन्म लेती हैं। युवावस्था में तो तुम्हारे शरीर को अधिक ऊर्जा और ताक़त की आवश्यकता होती है, जो तुम्हें केवल हरी सब्ज़ियों, फलों, दूध और घर के बने शुद्ध भोजन से ही मिल सकती है। 'पहला सुख निरोगी काया' (The first happiness is a healthy body) यह कहावत तुमने सुनी ही होगी।<br><br>

            मुझे पूरा विश्वास है कि तुम मेरी इस सलाह को गंभीरता से लोगे और आज से ही जंक फूड का त्याग कर संतुलित आहार (balanced diet) ग्रहण करना शुरू करोगे। अपने स्वास्थ्य का ध्यान रखना।<br><br>

            तुम्हारा बड़ा भाई,<br>
            विकास
        </div>

        <hr style="margin: 40px 0; border: 0; border-top: 1px solid var(--border-color);">

        <h2>3. The Congratulatory Letter (To a Friend)</h2>
        <p>A classic test of enthusiastic and literary (Sāhityik) vocabulary.</p>

        <div class="letter-box">
            <strong>प्रश्न:</strong> अपने मित्र को राज्य स्तर की वाद-विवाद प्रतियोगिता (State Level Debate Competition) में प्रथम पुरस्कार जीतने पर बधाई देते हुए पत्र लिखिए।<br><br>

            ८८, सिविल लाइंस,<br>
            प्रयागराज - २११००१<br><br>

            दिनांक: २२ अगस्त २०२६<br><br>

            प्रिय सखी राधिका,<br>
            सप्रेम नमस्ते।<br><br>

            यहाँ सब कुशल-मंगल है। आज सुबह जब मैंने समाचार पत्र पढ़ा तो उसमें तुम्हारी तस्वीर और राज्य स्तरीय वाद-विवाद प्रतियोगिता में तुम्हारे प्रथम आने का समाचार देखा। उस समाचार को पढ़कर सचमुच मेरी खुशी का ठिकाना न रहा। इस शानदार और ऐतिहासिक सफलता पर तुम्हें मेरी ओर से बहुत-बहुत हार्दिक बधाई! (Heartiest Congratulations)<br><br>

            राधिका, मुझे हमेशा से तुम्हारी तार्किक क्षमता (logical reasoning) और भाषा पर तुम्हारी पकड़ पर पूरा विश्वास था। तुम विद्यालय स्तर पर तो हमेशा जीतती ही आई हो, लेकिन आज तुमने पूरे राज्य में अपने माता-पिता और विद्यालय का नाम रोशन कर दिया है। यह तुम्हारी निरंतर मेहनत और आत्मविश्वास का ही परिणाम है। तुम सचमुच हम सभी मित्रों के लिए एक बड़ी प्रेरणा (inspiration) बन गई हो। मेरी ईश्वर से यही प्रार्थना है कि तुम जीवन के हर क्षेत्र में इसी प्रकार सफलता के नए शिखर (new heights) छूती रहो।<br><br>

            इस महान अवसर पर अंकल और आंटी जी को भी मेरी ओर से बधाई और सादर प्रणाम कहना। जब मैं अगले महीने तुमसे मिलूंगी, तो हम सब मिलकर इस सफलता की शानदार पार्टी करेंगे।<br><br>

            तुम्हारी अभिन्न सहेली,<br>
            नेहा
        </div>

        <hr style="margin: 40px 0; border: 0; border-top: 1px solid var(--border-color);">

        <h2>4. The Apology Letter (To Grandfather)</h2>
        <p>Tests the tone of deep remorse and reverence combined.</p>

        <div class="letter-box">
            <strong>प्रश्न:</strong> गर्मी की छुट्टियों में अपने दादा जी के पास गाँव न जा पाने के कारण उनसे क्षमा माँगते हुए एक पत्र लिखिए।<br><br>

            परीक्षा भवन,<br>
            नई दिल्ली - ११०००२<br><br>

            दिनांक: १० मई २०२६<br><br>

            पूज्य दादा जी,<br>
            सादर चरण स्पर्श।<br><br>

            कल पिताजी ने फोन पर बताया कि आप और दादी जी गाँव में मेरी बहुत प्रतीक्षा कर रहे हैं। मुझे अच्छी तरह याद है कि मैंने आपसे वायदा किया था कि मैं इस वर्ष गर्मी की छुट्टियों के पूरे बीस दिन आपके पास गाँव में ही बिताऊँगा। लेकिन दादा जी, मैं इस पत्र के माध्यम से आपसे क्षमा माँगना चाहता हूँ, क्योंकि इस बार मेरा गाँव आना संभव नहीं हो सकेगा।<br><br>

            दरअसल, हमारे विद्यालय प्रबंधन ने इस वर्ष गर्मी की छुट्टियों में हम दसवीं कक्षा के छात्रों के लिए विशेष अतिरिक्त कक्षाएँ (Extra Board Classes) आयोजित करने का निर्णय लिया है। चूँकि इस साल मेरी बोर्ड की परीक्षाएँ हैं, और मेरे गणित के कुछ विषय अभी भी कमज़ोर हैं, इसलिए शिक्षकों ने मुझे इन कक्षाओं में उपस्थित रहना अनिवार्य बताया है। यह कक्षाएँ पूरे पंद्रह दिनों तक चलेंगी। इसके कारण मेरा पूरा कार्यक्रम (schedule) बदल गया है। मुझे पता है कि आपको और दादी जी को इस खबर से बहुत निराशा होगी, लेकिन मेरी पढ़ाई के भविष्य को देखते हुए मेरा यहाँ रुकना अत्यंत आवश्यक है।<br><br>

            मैं आपसे विनती करता हूँ कि आप मेरी मजबूरी को समझेंगे और मुझे क्षमा कर देंगे। जैसे ही मेरे ये विशेष कक्षाएँ समाप्त होंगी, मैं दिवाली की छुट्टियों में निश्चित रूप से आपसे मिलने आऊँगा। दादी जी को मेरा बहुत सारा प्यार और चरण स्पर्श कहना। अपने स्वास्थ्य का ध्यान रखिएगा।<br><br>

            आपका पोता,<br>
            क. ख. ग.
        </div>

