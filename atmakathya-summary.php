            <h2 class="poetry-header">जीवन की रिक्तता और अभाव</h2>
            <div class="content-block">
                The poet speaks of his heart as an 'empty pot' (Gagar Reeti). He tells his friends that if he writes his
                story, they will realize that his life has nothing to offer but void. He is weary of people finding joy
                in his shortcomings. Prasad’s humility shines through as he claims that he hasn't achieved anything
                "great" enough to be etched in history.
            </div>
        </section>

        <section class="poetry-card">
            <h2 class="poetry-header">किंतु कहीं ऐसा न हो... (Refusal Reason)</h2>
            <div class="strophe-box">
                <span class="strophe-hindi">किंतु कहीं ऐसा न हो कि तुम ही खाली करने वाले,<br>अपने को समझो, मेरा रस ले अपनी भरने वाले।</span>
                The poet warns his friends that their curiosity might lead to realization that they are the ones who
                emptied his life of joy (took 'Ras' from him). He values his small, private 'Gatha' (story) and doesn't
                want it to be a subject of public scrutiny. He believes his "painless" past is better left unsaid.
            </div>
        </section>

