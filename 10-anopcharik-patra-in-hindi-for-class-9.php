            भेजा हुआ पार्सल मिला, जिसमें एक बहुत ही सुंदर कलाई घड़ी थी। इस शानदार उपहार और आपके आशीर्वाद के लिए हृदय से
            धन्यवाद। यह घड़ी मुझे हमेशा समय के पाबंद (punctual) रहने की याद दिलाएगी। मामी जी को प्रणाम।<br><br>
            आपका भानजा, मोहित
