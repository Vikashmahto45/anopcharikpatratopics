            किया हुआ भी भूल जाता है। एक अच्छी समय-सारणी (timetable) बनाओ और बीच-बीच में थोड़ा आराम करो। हम सब जानते हैं
            कि तुम एक मेधावी छात्र हो। शांत मन से तैयारी करो। शुभकामनाओं सहित।<br><br>
            तुम्हारा मित्र, कुणाल
        </div>

