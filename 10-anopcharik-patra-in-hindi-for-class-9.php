            परंतु इसका अत्यधिक उपयोग तुम्हारी आँखों और भविष्य दोनों के लिए बुरा है। अभी तुम्हारा पूरा ध्यान तुम्हारी
            पढ़ाई पर होना चाहिए। समय का सदुपयोग करो। अपना ध्यान रखना।<br><br>
            तुम्हारा बड़ा भाई, विहान
        </div>

