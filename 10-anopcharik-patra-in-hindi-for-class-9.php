            तुम्हें यह जानकर बहुत खुशी होगी कि पूरे शहर के २५ विद्यालयों के बीच मैंने अपनी तर्क-शक्ति से प्रथम स्थान
            प्राप्त कर 'स्वर्ण पदक' जीता है। मैं अपनी यह खुशी सबसे पहले तुम्हारे साथ साझा करना चाहता था। अंकल जी को
