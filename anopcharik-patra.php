        <h2>Essential Tips for Max Marks</h2>
        <ul>
            <li><strong>No Subject Line:</strong> The biggest mistake students make is writing "विषय" (Subject). An <strong>anopcharik patra</strong> never ever has a subject line.</li>
            <li><strong>Emotions over Formalities:</strong> Write exactly as you would speak to your relative, but avoid modern slang or internet shortcuts.</li>
            <li><strong>The Three-Part Body:</strong> Always stick to the 3-paragraph rule (Opening Health Check > Main Reason > Closing Regards to family).</li>
        </ul>

