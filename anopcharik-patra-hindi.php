        <h2>The Top 5 Grammatical Pitfalls in an Anopcharik Patra Hindi</h2>
        <p>Examiners actively look for these specific five mistakes. If you avoid them, you immediately place yourself
            in the top percentile of students.</p>

        <h3>1. The 'Poojniya' Disaster (पूजनीय vs पुज्यनीय)</h3>
        <p>When writing to an elder (Grandfather, Father), the highest level of respect is denoted by the word
            "Poojniya" (worthy of worship). Over 60% of students spell this word incorrectly in the exam hall.</p>
        <ul style="border-left: 4px solid var(--brand-dark); padding-left: 15px; background: var(--surface);">
            <li><strong>INCORRECT:</strong> पुज्यनीय (Pujyaniya) - This word does not exist in the Hindi dictionary.
            </li>
            <li><strong>INCORRECT:</strong> पूज्यनीय (Poojyaniya) - Structurally invalid.</li>
            <li><strong>CORRECT (For Male):</strong> पूजनीय (Poojniya) - e.g., पूजनीय पिताजी।</li>
            <li><strong>CORRECT (For Female):</strong> पूजनीया (Poojniyaa) - e.g., पूजनीया माताजी।</li>
        </ul>

        <h3>2. The 'Aashirvad' Conundrum (आशीर्वाद vs आर्शीवाद)</h3>
        <p>When an elder writes to a younger sibling, they offer blessings. The placement of the 'Ra' (र) matra is the
            most commonly butchered grammatical rule in a standard <strong>anopcharik patra hindi</strong>.</p>
        <ul style="border-left: 4px solid var(--brand-dark); padding-left: 15px; background: var(--surface);">
            <li><strong>INCORRECT:</strong> आर्शीवाद (Aarshivad) - The 'ra' is placed before the 'sha', changing the
                pronunciation entirely.</li>
            <li><strong>CORRECT:</strong> आशीर्वाद (Aashirvaad) - The 'ra' sound comes after 'sha' and before 'va',
                hence the flying 'reph' is placed on 'va'.</li>
        </ul>

        <h3>3. The Date Formatting Errors (दिनांक)</h3>
        <p>Examiners heavily deduct marks if you use English months written in the Roman script or if you use slashes.
        </p>
        <ul style="border-left: 4px solid var(--brand-dark); padding-left: 15px; background: var(--surface);">
            <li><strong>INCORRECT:</strong> 15/08/2026 OR 15th August 2026 (Written in Hindi paper).</li>
            <li><strong>CORRECT:</strong> १५ अगस्त २०२६ (Fully spelled out month in Devanagari).</li>
        </ul>

        <h3>4. The Plurality of Respect (आप vs तुम)</h3>
        <p>In Hindi, respect is demonstrated not just by the salutation, but by the conjugation of the verb at the end
            of the sentence. If you are writing to someone older, the verb MUST be plural. If you are writing to a
            younger person, the verb is singular.</p>
        <ul style="border-left: 4px solid var(--brand-dark); padding-left: 15px; background: var(--surface);">
            <li><strong>Writing to Father (Elder - Plural Verb):</strong> पिताजी, आप घर <em>आ जाइए</em>। (Father, please
                come home.)</li>
            <li><strong>Writing to Brother (Younger - Singular Verb):</strong> राहुल, तुम घर <em>आ जाओ</em>। (Rahul,
                come home.)</li>
        </ul>

        <h3>5. Salutations Gender Matching</h3>
        <p>The adjectives you use in your closing line must match your own gender to score a perfect 5/5 in an
            <em>anopcharik patra hindi</em>.</p>
        <ul style="border-left: 4px solid var(--brand-dark); padding-left: 15px; background: var(--surface);">
            <li><strong>A Boy writing to Mother:</strong> आपका आज्ञाकारी पुत्र (Your obedient son).</li>
            <li><strong>A Girl writing to Mother:</strong> आपकी आज्ञाकारी पुत्री / आपकी स्नेहमयी पुत्री (Your obedient
                daughter).</li>
        </ul>

