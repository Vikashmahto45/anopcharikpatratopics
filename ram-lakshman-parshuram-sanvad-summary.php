                    <li><strong>Anuprasa (अनुप्रास):</strong> Repetition of sounds in many lines for rhythm.</li>
                    <li><strong>Ulpreksha (उत्प्रेक्षा):</strong> Used by Tulsidas to create vivid imagery of the
                        conflict.</li>
