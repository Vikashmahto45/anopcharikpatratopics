        <ol style="margin-left: 20px; font-size: 1.1rem; line-height: 2;">
            <li><strong>१.</strong> आप छात्रावास में रहते हैं और आपकी माता जी का स्वास्थ्य पिछले कुछ दिनों से खराब है।
                उनके स्वास्थ्य की जानकारी माँगते हुए तथा अपना ध्यान रखने का आग्रह करते हुए उन्हें एक पत्र लिखिए।</li>
            <li><strong>२.</strong> आपके दादा जी ने आपके जन्मदिन पर आपको 'पंचतंत्र की कहानियाँ' नामक एक अत्यंत
                ज्ञानवर्धक पुस्तक भेजी है। इस सुंदर उपहार के लिए धन्यवाद देते हुए उन्हें एक पत्र लिखिए।</li>
            <li><strong>३.</strong> आपको अपने विद्यालय की ओर से एक सप्ताह के लिए शिमला भ्रमण (Educational Tour) पर जाना
                है। इसके लिए अनुमति तथा ५००० रुपये मंगाने हेतु अपने पिताजी को एक पत्र लिखिए।</li>
            <li><strong>४.</strong> कक्षा में प्रथम स्थान प्राप्त करने पर आपके चाचा जी ने आपको एक नई साइकिल उपहार में दी
                है। इस स्नेह के लिए उनके प्रति कृतज्ञता (gratitude) व्यक्त करते हुए एक पत्र लिखिए।</li>
            <li><strong>५.</strong> आप अपनी बुआ जी के यहाँ छुट्टियाँ बिताने गए थे और वहाँ से वापस लौट आए हैं। उनके
                द्वारा की गई मेहमानवाज़ी (hospitality) और प्रेम के लिए धन्यवाद देते हुए उन्हें पत्र लिखिए।</li>
        </ol>

        <h2>Section B: The Equal Tone (Friends & Cousins)</h2>
        <p>These <strong>anopcharik patra questions</strong> evaluate the domain of 'Mitravata' (Friendship). The tone
            must be enthusiastic, nostalgic, and supportive. Use "प्रिय मित्र" and "सप्रेम नमस्ते".</p>

        <ol style="margin-left: 20px; font-size: 1.1rem; line-height: 2;" start="6">
            <li><strong>६.</strong> अपने मित्र को राज्य स्तरीय क्रिकेट टूर्नामेंट (Cricket Tournament) में चयन होने पर
                हार्दिक बधाई देते हुए एक उत्साहपूर्ण पत्र लिखिए।</li>
            <li><strong>७.</strong> आपका मित्र अपनी बोर्ड परीक्षा के परिणामों को लेकर भयंकर तनाव (Stress) में है। उसे
                सांत्वना देते हुए और परीक्षा से डरने के बजाय मेहनत करने की प्रेरणा देते हुए पत्र लिखिए।</li>
            <li><strong>८.</strong> गर्मी की छुट्टियों में अपने एक शहरी मित्र को अपने गाँव आमंत्रित करते हुए पत्र लिखिए,
                जिसमें गाँव की सुंदरता और स्वच्छ वातावरण का वर्णन हो।</li>
            <li><strong>९.</strong> पिछले सप्ताह आपके विद्यालय में 'स्वच्छता अभियान' का आयोजन हुआ था। अपने मित्र को उस
                अभियान में अपने अनुभव और योगदान के बारे में विस्तार से बताते हुए पत्र लिखिए।</li>
            <li><strong>१०.</strong> आपके मित्र के बड़े भाई का अचानक दुर्घटना में निधन हो गया है। इस अत्यंत पीड़ादायक
                स्थिति में अपनी गहरी संवेदना (Condolence) व्यक्त करते हुए मित्र को एक सांत्वना पत्र लिखिए।</li>
        </ol>

        <h2>Section C: The Guiding Tone (Youngers)</h2>
        <p>These prompts are examiner favorites. They test your maturity by asking you to guide someone younger. Use
            "प्रिय अनुज" and "शुभाशीर्वाद".</p>

        <ol style="margin-left: 20px; font-size: 1.1rem; line-height: 2;" start="11">
            <li><strong>११.</strong> आपका छोटा भाई आजकल अपना सारा समय मोबाइल फोन और ऑनलाइन गेमिंग (Online Gaming) में
                व्यर्थ कर रहा है। उसे समय का महत्व समझाते हुए और इस बुरी आदत से बचने की सलाह देते हुए पत्र लिखिए।</li>
            <li><strong>१२.</strong> अपने छोटे भाई को जंक फूड (Junk Food) खाने से होने वाले शारीरिक नुकसान बताते हुए उसे
                संतुलित और पौष्टिक आहार खाने का सुझाव देते हुए एक पत्र लिखिए।</li>
            <li><strong>१३.</strong> आपकी छोटी बहन पुस्तकालय (Library) का बिल्कुल उपयोग नहीं करती। उसे अच्छी और
                ज्ञानवर्धक पुस्तकें पढ़ने की प्रेरणा देते हुए एक विस्तृत पत्र लिखिए।</li>
            <li><strong>१४.</strong> दसवीं कक्षा उत्तीर्ण करने के बाद आपका छोटा भाई विषय-चयन (Stream selection) को लेकर
                दुविधा में है। उसे उसकी रुचि के अनुसार सही निर्णय लेने के लिए मार्गदर्शन करते हुए पत्र लिखिए।</li>
            <li><strong>१५.</strong> अपने छोटे भाई को सुबह जल्दी उठकर व्यायाम और योग (Yoga) करने के लाभ बताते हुए एक
                प्रेरणादायक पत्र लिखिए ताकि उसका स्वास्थ्य उत्तम रहे।</li>
        </ol>

