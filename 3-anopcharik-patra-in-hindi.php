            मित्र, मुझे अच्छी तरह याद है कि कैसे हम दोनों बारिश के दिनों में कागज़ की नावें बनाकर पानी में बहाया करते थे। विद्यालय के मैदान में वह क्रिकेट का खेल, और गणित के मास्टर जी की डांट खाने के बाद एक-दूसरे का चेहरा देखकर छुप-छुप कर हँसना—वो दिन वास्तव में कितने मासूम और चिंता-मुक्त थे। समय कितनी तेज़ी से बीत गया है। आज हम दोनों अपनी-अपनी नौकरी और जीवन की भागदौड़ में इतने व्यस्त हो गए हैं कि महीनों तक हमारी आपस में बात भी नहीं हो पाती। परंतु यकीन मानो, मेरे हृदय में आज भी उन यादों का स्थान सबसे ऊपर है।<br><br>

