            पिताजी, मैं अपनी उस दिन की मूर्खतापूर्ण हरकत के लिए आपसे हाथ जोड़कर और सच्चे हृदय से क्षमा माँगता हूँ। मैं कुछ गलत मित्रों की संगति में आकर अपना विवेक (wisdom) खो बैठा था, और उसी नासमझी में मैंने वह अनुचित कार्य किया जिसकी वजह से आपको प्रधानाचार्य जी के सामने शर्मिंदा होना पड़ा। मुझे अपनी इस गलती का गहरा पश्चाताप है। मैं आपको यह पूर्ण विश्वास दिलाता हूँ कि भविष्य में मेरे द्वारा ऐसा कोई भी कार्य नहीं होगा जिससे आपके मस्तक पर कोई दाग लगे या आपको नीचा देखना पड़े।<br><br>

